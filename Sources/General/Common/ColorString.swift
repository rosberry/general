public func yellow(_ string: String) -> String {
    ColorChars.yellow + string + ColorChars.default
}

public func green(_ string: String) -> String {
    ColorChars.green + string + ColorChars.default
}

public func red(_ string: String) -> String {
    ColorChars.red + string + ColorChars.default
}
